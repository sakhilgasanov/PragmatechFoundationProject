a=int(input("a ededini daxil edin: "))
b=int(input("b ededini daxil edin: "))
emeliyyat=input("emeliyyati daxil edin: ")
if emeliyyat=="+":
    print(a+b)
elif emeliyyat=="-":
    print(a-b)
elif emeliyyat=="*":
    print(a*b)
elif emeliyyat=="/":
    print(a/b)
else:
    print("daxil etdiyiniz emeliyyat duzgun deyil")
    